int32_t ov5640_sensor_power_up(struct msm_sensor_ctrl_t *s_ctrl)
{
	int32_t rc = -EINVAL;
	pr_err("OV5640: Enter: Power Up");

	msleep(20);

	rc = msm_sensor_power_up(s_ctrl);
	if (rc < 0) {
		pr_err("%s: Exit: msm_sensor_power_up failed\n", __func__);
		return rc;
	}

	pr_err("OV5640: Exit: Power Up");
	return rc;
}

